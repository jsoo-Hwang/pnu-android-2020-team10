package mapactivity;

public class PlaceData {
    private String name;
    private String add;


    public PlaceData(String name, String add)
    {
        this.name = name;
        this.add = add;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAdd() {
        return add;
    }

    public void setAdd(String add) {
        this.add = add;
    }
}
